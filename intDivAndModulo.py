# // is integer division, meaning it rounds down to the nearest whole number

print("5 // 2 is", 5 // 2)

print("9 // 12 is", 9 // 12)

print("10 // 3 is", 10 // 3)

# % (pronounced "modulo") remainder operator, gives remainder from dividing

print("5 % 2 is", 5 % 2)

print("9 % 12 is", 9 % 12)

print("10 % 3 is", 10 % 3)
